void main(void)
{
// main entry point
_DISABLE_COP();

/********************************************************************/
// initializations
/********************************************************************/
SevSeg_ButtInit();
LCD_Init();

LCD_MyInitials();


	for (;;)
	{
	//main program loop
	  if (PT1AD1 & 0b00000100) // bottom
	  {
	      LCD_Addr(0x58);
	      LCD_String("Hola World!!");
	  }
	  
	  if (PT1AD1 & 0b00010000) // top
	  {
	      LCD_Ctrl(0b00000001);
	      LCD_Ctrl(0b00001110);
	      LCD_Addr(0x0);
	  }
	  
	  if (PT1AD1 & 0b00000001) // middle
	      LCD_MyInitials();
	}

}



